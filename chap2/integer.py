# 整数类型：可以表示正数，负数，0
n1 = 90
n2 = 76
n3 = 0
print(n1, type(n1))
print(n2, type(n2))
print(n3, type(n3))

# 整数可以表示为二进制0b，十进制，八进制0o，十六进制0x
print('十进制', 118)  # 默认为十进制
print('二进制', 0b10101111)  # 0b二进制
print('八进制', 0o176)  # 0o八进制
print('十六进制', 0x1EAF)  # 0x十六进制
