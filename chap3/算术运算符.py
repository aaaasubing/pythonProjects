print(1+1)  # 加法运算
print(1-1)
print(2*4)
print(4/2)
print(11/5)  # 5.5，除法运算
print(11//2)  # 5，整除运算
print(11 % 2)  # 取余运算
print(2**2)  # 表示的是2的2次方
print(2**3)  # 表示的是2的3次方

# 存在正负数的情况
print(9//4)  # 2
print(-9//-4)  # 2
# 一正一负整除，向下取整
print(9//-4)  # -3
print(-9//4)  # -3
# 一正一负取余，公式：余数=被除数-除数*商
print(9 % -4)  # -3
print(-9 % 4)  # 3
